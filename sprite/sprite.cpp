#include "sprite.h"

void drawSpriteBuffer(Buffer *buffer, const Sprite &sprite, size_t x, size_t y, uint32_t color) {
    for (size_t xi = 0; xi < sprite.width; ++xi) {
        for (size_t yi = 0; yi < sprite.height; ++yi) {
            size_t sy = sprite.height - 1 + y - yi;
            size_t sx = x + xi;

            if (sprite.data[yi * sprite.width + xi] && sy < buffer->height && sx < buffer->width) {
                buffer->data[sy * buffer->width + sx] = color;
            }
        }
    }
}

void
drawTextBuffer(Buffer *buffer, const Sprite &textSpriteSheet, const char *text, size_t x, size_t y, uint32_t color) {
    size_t xp = x;
    size_t stride = textSpriteSheet.width * textSpriteSheet.height;
    Sprite sprite = textSpriteSheet;
    for (const char *charp = text; *charp != '\0'; ++charp) {
        char character = *charp - 32;
        if (character < 0 || character >= 65) continue;

        sprite.data = textSpriteSheet.data + character * stride;
        drawSpriteBuffer(buffer, sprite, xp, y, color);
        xp += sprite.width + 1;
    }
}

void drawNumberBuffer(Buffer* buffer, const Sprite& numberSpriteSheet, size_t number, size_t x, size_t y, uint32_t color)
{
    uint8_t digits[64];
    size_t numberOfDigits = 0;

    size_t currentNumber = number;
    do
    {
        digits[numberOfDigits++] = currentNumber % 10;
        currentNumber = currentNumber / 10;
    }
    while(currentNumber > 0);

    size_t xp = x;
    size_t stride = numberSpriteSheet.width * numberSpriteSheet.height;
    Sprite sprite = numberSpriteSheet;
    for(size_t i = 0; i < numberOfDigits; ++i) {
        uint8_t digit = digits[numberOfDigits - i - 1];
        sprite.data = numberSpriteSheet.data + digit * stride;
        drawSpriteBuffer(buffer, sprite, xp, y, color);
        xp += sprite.width + 1;
    }
}

bool checkSpriteOverlap(
        const Sprite &sp_a, size_t x_a, size_t y_a,
        const Sprite &sp_b, size_t x_b, size_t y_b
) {
    // NOTE: For simplicity we just check for overlap of the sprite
    // rectangles. Instead, if the rectangles overlap, we should
    // further check if any pixel of sprite A overlap with any of
    // sprite B.
    if (x_a < x_b + sp_b.width && x_a + sp_a.width > x_b &&
        y_a < y_b + sp_b.height && y_a + sp_a.height > y_b) {
        return true;
    }

    return false;
}

